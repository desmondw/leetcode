/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @return {boolean}
 */
var isPalindrome = function(head) {
  let list = []
  let node = head
  while (node) {
    list.push(node.val)
    node = node.next
  }
  
  return list.join() == list.reverse().join()
};
